package com.operators;

public class AssignmentOperators {

	public static void main(String[] args) {
		System.out.println("Main!!!!!!!!!!!");
		double result=0;
		double x=5.5;
		result =result+x;
		System.out.println(result);
		
		result +=x;
		System.out.println(result);
		double y=3.9;
		result -=y;
		System.out.println(result);
		double w=5.5;
		result=result*w;
		result *=w;
		System.out.println(result);
		
		result /=x;
		System.out.println(result);
		
		result %=x;
		System.out.println(result);
	}

}
